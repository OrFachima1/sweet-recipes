// פונקציה לקבלת קטגוריה של מנה
export function getItemCategory(itemTitle: string): string {
  return ITEM_TO_CATEGORY[itemTitle] || "אחר";
}

// פונקציה לקבלת צבע של קטגוריה
export function getCategoryColor(category: string): string {
  return CATEGORY_COLORS[category] || "#E5E7EB"; // אפור ברירת מחדל
}

// פונקציה לקיבוץ פריטים לפי קטגוריה
export function groupItemsByCategory(items: any[]): Record<string, any[]> {
  const grouped: Record<string, any[]> = {};
  
  items.forEach(item => {
    const category = getItemCategory(item.title);
    if (!grouped[category]) {
      grouped[category] = [];
    }
    grouped[category].push(item);
  });
  
  return grouped;
}

